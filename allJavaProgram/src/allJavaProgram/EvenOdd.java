package allJavaProgram;

public class EvenOdd {

	public static void main(String[] args) {
		int num=10;
      if(num%2==0)
      {
    	  System.out.println("Given number is even ="+num);
      }
      else
      {
    	  System.out.println("Given number is odd  ="+num);
      }
	}

}
